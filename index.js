$('button').click(function(){
    const s1Count = parseInt($('#s1-input').val());
    const s2Count = parseInt($('#s2-input').val());
    const s3Count = parseInt($('#s3-input').val());
    const totalCount = s1Count + s2Count + s3Count;
    if (totalCount > 5) {
        console.log("Total is greater than 5. Not Allowed");
    } else {
        $('.chance-text').empty();
        $('.chance-text').text(`Chance: ${findPercentage()}%`)
    }
});

function findPercentage() {
    let totalCombos = createSet();
    let maxPoints = [$('#s1-input').attr('max'),$('#s2-input').attr('max'),$('#s3-input').attr('max')];
    let specifiedPoints = [$('#s1-input').val(),$('#s2-input').val(),$('#s3-input').val()];

    maxPoints = maxPoints.map(pointValue => parseInt(pointValue));
    specifiedPoints = specifiedPoints.map(pointValue => parseInt(pointValue));

    totalCombos = elimImpossibleCombos(maxPoints,totalCombos);
    possibleCombos = selectPossibleCombos(specifiedPoints,totalCombos);
    
    return Math.round((possibleCombos.length / totalCombos.length) * 10000) / 100;
    
}

function createSet() {
    let combinations = [];
    for(let i = 0; i < 243; i++) {
        let ternaryNum = i.toString(3);
        while (ternaryNum.length < 5) {
            ternaryNum = '0' + ternaryNum;
        }
        combinations.push(ternaryNum);
    }
    return combinations
}

function countDigits(combination,index) {
    let count = 0;
        for(let i=0;i<combination.length;i++) {
            if (parseInt(combination[i]) === index)
            count++;
        }
    return count;
}

function elimImpossibleCombos(pointsAmount,combinations) {
    pointsAmount.forEach(function(pointValue,index){
        combinations = combinations.filter(function(combination) {
            let count = countDigits(combination,index);
            return count <= pointValue
        });
    });
    return combinations;
}

function selectPossibleCombos(pointsAmount,combinations) {
    pointsAmount.forEach(function(pointValue,index){
        if (pointValue === 0) {
            return;
        }
        combinations = combinations.filter(function(combination) {
            let count = countDigits(combination,index);
            return count >= pointValue
        });
    });
    return combinations;
}